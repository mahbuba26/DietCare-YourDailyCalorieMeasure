package com.mahbuba.dietcare;

public class Pointvalue {
    long x;
    long  y;

    public Pointvalue(){}
    public Pointvalue(long x, long y){
        this.x=x;
        this.y=y;

    }
    public double getX(){
        return x;

    }
    public void setX(long x){
        this.x=x;
    }
    public  double getY(){
        return  y;
    }

    public void setY(long y){
        this.y=y;
    }


}



